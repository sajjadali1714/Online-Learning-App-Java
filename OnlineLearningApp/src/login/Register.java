    public boolean isValidate() {
        boolean v_retuen = false;
        try {
            if (txtName.getText().length() < 3) {
                MsgBox.showMsg("Name must be at least 3 characters.");
                v_retuen = false;
            } else if (txtEmail.getText().length() < 5) {
                MsgBox.showMsg("Email must be at least 5 characters.");
                v_retuen = false;
            } else if (txtUserName.getText().length() < 5) {
                MsgBox.showMsg("User name must be at least 5 characters.");
                v_retuen = false;
            } else if (txtPassword.getPassword().length < 5) {
                MsgBox.showMsg("Password must be at least 5 characters.");
                v_retuen = false;
            } else{
                v_retuen = true;
            }            
        } catch (Exception e) {

        }
        return v_retuen;
    }

