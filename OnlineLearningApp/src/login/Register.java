    public boolean RegisterUser(String fullName, String email_id, String user_name, String pwd) {
        boolean v_return = false;
        try {
            String sql = "";
            sql = "insert into tbl_user(fullName,email_id,user_name,pwd) "
                    + " values ('" + fullName + "','" + email_id + "','" + user_name + "','" + pwd + "')";
            DBOperations dbOperations = new DBOperations();
            v_return = dbOperations.InsertRecord(sql);
        } catch (Exception e) {
            System.out.println("Error in Register User " + e.getMessage());
        }
        return v_return;
    }

